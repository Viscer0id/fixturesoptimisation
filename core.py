# Genetic algorithm rostering
#
# 336 teams get split into 40 competitions of between 6 to 9 teams each.
# The teams in each competition are closely matched in proficiency, this ensures a good game.
# The number of matches each team plays is dependant on the number of teams in the competition.
# Each team plays one match per round.
# We need to cap the total number of rounds to 15, so:
#   if there are 6 teams, each team plays every other team 3 times: (6-1)*3 = 15 rounds.
#   if there are 8 teams, each team plays every other team 2 times: (8-7)*2 = 14 rounds.
# A round is one calendar week.
# Each match is played at a venue
# Each venue is limited in space, so depending on the competition, might be able to play multiple matches simultaneously

